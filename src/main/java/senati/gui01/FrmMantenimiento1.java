void verEmpresas(){
    DefaultTableModel modelo=new DefaultTableModel();
    modelo.addColumn("Codigo");
    modelo.addColumn("Compa√±ia");
    modelo.addColumn("Telefono");
    try{
        obj.obtenerConexion();
        obj.st=obj.conectar.createStatement();
        obj.rs=obj.st.executeQuery("select * from Shippers;");
        while(obj.rs.next()){
            Object data[]={obj.rs.getString(1),
            obj.rs.getString(2),
            obj.rs.getString(3)
             };
        modelo.addRow(data);
       
        }
        tbVista1.setModel(modelo);
        obj.rs.close();
        obj.conectar.close();
                
    }catch(SQLException e){
        e.getMessage().toString();
    }
        
}

void buscarEmpresa(int cod){
    
    try{
       obj.obtenerConexion();
      PreparedStatement pst=obj.conectar.prepareStatement("select * from Shippers where ShipperID = ?;");
      pst.setInt(1, cod);
      obj.rs=pst.executeQuery();
      if(obj.rs.next()){
          txtNOmbre.setText(obj.rs.getString(2));
          txtTele.setText(obj.rs.getString(3));
      }else{
          txtNOmbre.setText(obj.rs.getString(2));
          txtTele.setText(obj.rs.getString(3));
          mensaje("El codigo no exite");
      }
      obj.rs.close();
      obj.conectar.close();
        
    }catch(SQLException e){
        mensaje(e.toString());
    }
    
    
}

